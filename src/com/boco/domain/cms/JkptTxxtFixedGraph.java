package com.boco.domain.cms;

import java.io.Serializable;

/**
 * 可变情报板播放表中的固定图标
 * @author 孙冠义
 *
 */
public class JkptTxxtFixedGraph implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4809460144981986693L;
	/**
	 * 固定图标标识
	 */
	private Integer fixedgraphid; 
	/**
	 * 图标编号
	 */
	private String fixedgraphisn;
	/**
	 * 图标描述
	 */
	private String fixedgraphdesc;
	/**
	 * 设备类型
	 */
	private Integer commandType; 
	/**
	 * 固定值
	 */
	private Integer fixValue;
	/**
	 * @return the 固定图标标识
	 */
	public Integer getFixedgraphid() {
		return fixedgraphid;
	}
	/**
	 * @param 固定图标标识 the fixedgraphid to set
	 */
	public void setFixedgraphid(Integer fixedgraphid) {
		this.fixedgraphid = fixedgraphid;
	}
	/**
	 * @return the 图标编号
	 */
	public String getFixedgraphisn() {
		return fixedgraphisn;
	}
	/**
	 * @param 图标编号 the fixedgraphisn to set
	 */
	public void setFixedgraphisn(String fixedgraphisn) {
		this.fixedgraphisn = fixedgraphisn;
	}
	/**
	 * @return the 图标描述
	 */
	public String getFixedgraphdesc() {
		return fixedgraphdesc;
	}
	/**
	 * @param 图标描述 the fixedgraphdesc to set
	 */
	public void setFixedgraphdesc(String fixedgraphdesc) {
		this.fixedgraphdesc = fixedgraphdesc;
	}
	/**
	 * @return the 设备类型
	 */
	public Integer getCommandType() {
		return commandType;
	}
	/**
	 * @param 设备类型 the commandType to set
	 */
	public void setCommandType(Integer commandType) {
		this.commandType = commandType;
	}
	/**
	 * @return the 固定值
	 */
	public Integer getFixValue() {
		return fixValue;
	}
	/**
	 * @param 固定值 the fixValue to set
	 */
	public void setFixValue(Integer fixValue) {
		this.fixValue = fixValue;
	}	
}
